#include "time.h"


/* ****************************************************************
** 函 数 名: Tim0Init() FOSC/4
** 功能描述: 定时器初始化 使用库函数
*************************************************************** */
void Tim0Init()
{
    OpenTimer0( TIMER_INT_ON &
                 T0_16BIT &
              T0_SOURCE_INT &
                T0_PS_1_2 );
    WriteTimer0( 15635 );//50ms中断一次

}

